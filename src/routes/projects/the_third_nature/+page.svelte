    <h3>Abstract</h3>
    <p>
        Hydrology, the key element throughout the long journey of human civilization, has evolved in multiple forms. The dam is one of the most significant elements. Once hailed as the key element of clean energy, dams have been questioned today. A reconsideration of their legitimacy and their future has become one of the key debates of the world infrastructure system. While establishing larger and larger-scale dams and reservoirs still takes place, decommissioning old and small ones has become a rational choice for many countries' decision-makers.
    </p>
    <p>
        This research focuses on the process of "decommissioning," with a special concern on mainland China, the largest country in terms of installed hydropower capacity and generation capacity since 2014. Here, "decommissioning" is defined as a government-led, planned process of removing the physical existence of such infrastructure or disabling or limiting its functionality, especially for obsolete or small-sized dams (installed capacity of fewer than 50,000 kilowatts).
    </p>

