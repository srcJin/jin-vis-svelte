  <h1>Silkroad Urbanism</h1>
  <h3>The Making of Cities Course Research</h3>
  <h3>Collaborator: Yufei Chen, Advisor: Roi Salgueiro Barrio</h3>

  <p>
    Since the Roman Period, the Silk Road has always been a corridor that serves
    as an infrastructure contributing to the territorial organization,
    interconnecting the cities, and gaining connectivity. The transfer and
    interaction of cultures led to the formation of numerous architectural
    elements that sprung up along this corridor. Today, a series of modern
    urbanization is underway with the rapid implementation of the Belt and Road
    Initiative, the world's largest infrastructure project in decades.
    Positioning ourselves as the mediators between past, present, and future, we
    focus on how Asian civilizations have gained influence in the mutually
    restrictive Indo-European cultural circles through commodity economies,
    material, and economic exchanges. Through architectural elements, urban
    texture, and citizens' behavior, we can reveal the hidden connection that
    reflects the Silk Road as a driving force and a negotiation tool for
    international geopolitical cooperation.
  </p>
