  <p>
    The map reinforces the existence of the Southern China-Europe Railway that
    went across Istanbul city. With the establishment of this railway route, it
    becomes possible to travel from Xi'an to Turkey within 12 days -- one-half
    to one-fourth compared with sea transportation. The railway connection
