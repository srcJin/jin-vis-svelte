  <h4>World City Connected by the Aerial Silk Road</h4>
  <p>
    The "Air Silk Road" can overcome the infrastructure problems associated with
    land transportation and the geographical limitations of maritime transport.
    Therefore it becomes a vital supplement for high-value and time-sensitive
    goods.
  </p>
  <p>
    Over 371 cities is connected by Istanbul airport, and 96 of them provide
    cargo accessibilities.[2] Within 12 hours of flight, passengers can travel
    to most parts of the world - even less with the future ultrasonic planes.
    Airports provide a modern urban interface, and airplanes are the high-speed
    data lines between cities. Working together, airports in all major cities
    weave an intersecting network, which also acts as the solid infrastructure
    for the so-called 'global cities.' We want to use this map to show how much
    of a role the establishment of an airport can play in the growth of the
    global cities.
  </p>

