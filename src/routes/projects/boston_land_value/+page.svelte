  <h2>Abstract</h2>
  <p>
    Boston is a city we are most familiar with that has accessible land value
    and geographical data. Because of the rich historical context of the city,
    it consists of distinct urban form typologies within a certain range so that
    we can limit the influence from other factors. In this project, we want to
    reveal the relationship between land prices and different kinds of urban
    form, to find how urban form affects land price based on facts, and form a
    guideline for urban designers, real estate developers, policymakers, and
    planners to make decisions for more equitable cities.
  </p>

