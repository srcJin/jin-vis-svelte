  <h2>Discussion</h2>
  <p>
    The preliminary findings of this topic do reveal some relationship between
    urban form and land value. But they are not conclusive. Land value can be
    affected by so many factors and is changing throughout the time, especially
    in the internet era that the popularity of one place can be changed within
    hours. So this topic could be further explored by making it more responsive
    and consider more parameters, finding the major impact factor and revealing
    the value change throughout time.
  </p>

